    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        if (txtNomeCliente.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "O campo nome é obrigatório!");
            return;
        }

        if (txtCPF.getText().replace(".", "").replace("-", "").trim().equals("")) {
            JOptionPane.showMessageDialog(this, "O Campo CPF é obrigatório!");
            return;
        }

        if (txtSexo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "O campo Sexo é obrigatório!");
            return;
        }

        if (txtEstadoCivil.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "O campo Estado Civil é obrigatório!");
            return;
        }

        if (txtEmail.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "O campo Email é obrigatório!");
            return;
        }

        if (txtTelefone.getText().replace("(", "").replace(")", "").replace("-", "").trim().equals("")) {
            JOptionPane.showMessageDialog(this, "O Campo Telefone é obrigatório!");
            return;
        }

        if (txtDataCliente.getDate().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "O campo Data de nascimento é obrigatório!");
            return;
        }

        jTabbedPane1.setSelectedIndex(1);
    }//GEN-LAST:event_btnProximoActionPerformed

