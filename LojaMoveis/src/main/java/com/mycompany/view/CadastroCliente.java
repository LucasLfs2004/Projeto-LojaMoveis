    private void btnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirActionPerformed
        if (modoTela == "Criação") {
            String nome = txtNomeCliente.getText();
            String cpf = txtCPF.getText();
            String sexo = txtSexo.getText();
            String estadoCivil = txtEstadoCivil.getText();
            String email = txtEmail.getText();
            String telefone = txtTelefone.getText();
            Date data = txtDataCliente.getDate();
            System.out.println(data);
            String rua = txtRua.getText();
            String cep = txtCEP.getText();

            //Passo as informações para o objeto cliente (propriedade deste JFrame)
            objCliente.setNome(nome);
            objCliente.setCPF(cpf);
            objCliente.setSexo(sexo);
            objCliente.setEstadoCivil(estadoCivil);
            objCliente.setEmail(email);
            objCliente.setTelefone(telefone);
            objCliente.setDataCliente(data);
            objCliente.setRua(rua);
            objCliente.setCEP(cep);

            //Mando salvar no banco de dados usando a classe DAO
            try {
                ClienteDAO.salvar(objCliente);
                JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "Produto cadastrado", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao gravar no banco de dados\n" + e.getMessage(),
                        "Aviso de Falha", JOptionPane.ERROR_MESSAGE);
            }

        } else { //Modo de alteração

            //TODO: implementar verificação de obrigatoriedade
            String nome = txtNomeCliente.getText();
            String cpf = txtCPF.getText();
            String sexo = txtSexo.getText();
            String estadoCivil = txtEstadoCivil.getText();
            String email = txtEmail.getText();
            String telefone = txtTelefone.getText();
            Date data = txtDataCliente.getDate();
            String rua = txtRua.getText();
            String cep = txtCEP.getText();

            //Passo as informações para o objeto cliente (propriedade deste JFrame)
            objCliente.setNome(nome);
            objCliente.setCPF(cpf);
            objCliente.setSexo(sexo);
            objCliente.setEstadoCivil(estadoCivil);
            objCliente.setEmail(email);
            objCliente.setTelefone(telefone);
            objCliente.setDataCliente(data);
            objCliente.setRua(rua);
            objCliente.setCEP(cep);

            //Mando salvar no banco de dados usando a classe DAO
            try {
                boolean retorno = ClienteDAO.atualizar(objCliente);

                JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso!", "Produto atualizado", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao gravar no banco de dados\n" + e.getMessage(),
                        "Aviso de Falha", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_btnConcluirActionPerformed

