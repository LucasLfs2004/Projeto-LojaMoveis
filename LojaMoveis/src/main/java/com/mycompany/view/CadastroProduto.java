    private void btnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirActionPerformed
        if (modoTela == "Criação") {
            String tipo = txtTipoProduto.getText();
            Double preco = Double.valueOf(txtPreco.getText());
            String cor = txtCorProduto.getText();
            int quantidade = Integer.parseInt(txtQuantidadeProduto.getText());
            String descricao = txtDescricaoProduto.getText();

            //Passo as informações para o objeto cliente (propriedade deste JFrame)
            objProduto.setTipo(tipo);
            objProduto.setPreco(preco);
            objProduto.setCor(cor);
            objProduto.setQuantidade(quantidade);
            objProduto.setDescricao(descricao);

            //Mando salvar no banco de dados usando a classe DAO
            try {
                ProdutoDAO.salvar(objProduto);
                JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "Produto cadastrado", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao gravar no banco de dados\n" + e.getMessage(),
                        "Aviso de Falha", JOptionPane.ERROR_MESSAGE);
            }

        } else { //Modo de alteração

            //TODO: implementar verificação de obrigatoriedade
            String tipo = txtTipoProduto.getText();
            Double preco = Double.valueOf(txtPreco.getText());
            String cor = txtCorProduto.getText();
            int quantidade = Integer.parseInt(txtQuantidadeProduto.getText());
            String descricao = txtDescricaoProduto.getText();

            //Passo as informações para o objeto cliente (propriedade deste JFrame)
            objProduto.setTipo(tipo);
            objProduto.setPreco(preco);
            objProduto.setCor(cor);
            objProduto.setQuantidade(quantidade);
            objProduto.setDescricao(descricao);

            //Mando salvar no banco de dados usando a classe DAO
            try {
                boolean retorno = ProdutoDAO.atualizar(objProduto);

                JOptionPane.showMessageDialog(this, "Produto atualizado com sucesso!", "Produto atualizado", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao gravar no banco de dados\n" + e.getMessage(),
                        "Aviso de Falha", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_btnConcluirActionPerformed

