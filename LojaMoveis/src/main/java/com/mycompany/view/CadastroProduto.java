    private void txtPrecoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecoKeyTyped
        char c = evt.getKeyChar();
        if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE) && (c != KeyEvent.VK_PERIOD)) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Digite somente o valor do preço unitário por meio de números!");
        }
    }//GEN-LAST:event_txtPrecoKeyTyped

